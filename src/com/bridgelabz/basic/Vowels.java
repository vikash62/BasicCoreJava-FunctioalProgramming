package com.bridgelabz.basic;

public class Vowels {
	  public static void main(String[] args) {

	        char ch = 'z';

	        if(ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' )
	            System.out.println(ch + " is vowel");
	        else
	            System.out.println(ch + " is consonant");

	    }
}
